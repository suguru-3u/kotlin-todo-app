CREATE DATABASE IF NOT EXISTS kotlindatabase;
USE kotlindatabase;

CREATE TABLE IF NOT EXISTS POSTS (
    POST_ID BIGINT UNSIGNED NOT NULL COMMENT '投稿ID',
    TITLE VARCHAR(2000) COMMENT 'タイトル',
    POST_DATE DATETIME COMMENT '投稿日',
    DELETE_FLG boolean COMMENT '削除フラグ',
    PRIMARY KEY(POST_ID)
) COMMENT="TODO管理テーブル";